# Reverse mapping: name → code (for lookups)
NAME_TO_FIPS: dict[str, str] = {v: k for k, v in FIPS_TO_NAME.items()}


def code_to_name(code: str) -> str:
    """Return display name for a FIPS code, or the code itself if unknown."""
    return FIPS_TO_NAME.get(code.upper(), code)


def name_to_code(name: str) -> str:
    """Return FIPS code for a display name, or the name itself if unknown."""
    return NAME_TO_FIPS.get(name, name)
